//21)C++ Program to Multiply two Numbers

#include <iostream>
using namespace std;

int main(){
    int n1,n2,mul;

    cout<<"\nInput the 1st number : ";
    cin>>n1;
    cout<<"\nInput the 2nd number : ";
    cin>>n2;
    mul=n1*n2;
    cout<<"\n"<<n1<<" * "<<n2<<" = "<<mul;

    return 0;
}
